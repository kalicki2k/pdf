    public static function measureTextWidth(string $baseFont, string $text, float $size): ?float
    {
        if ($baseFont === StandardFont::SYMBOL->value) {
            return self::measureMappedTextWidth($text, $size, StandardFontSymbolMap::MAP, self::SYMBOL_WIDTHS);
        }

        if ($baseFont === StandardFont::ZAPF_DINGBATS->value) {
            return self::measureMappedTextWidth($text, $size, StandardFontZapfDingbatsMap::MAP, self::ZAPF_DINGBATS_WIDTHS);
        }

        $widths = self::WIDTHS[$baseFont] ?? null;

        if ($widths === null && str_starts_with($baseFont, 'Courier')) {
            $widths = self::buildCourierWidths();
        }

        if ($widths === null) {
            return null;
        }

        $width = 0;
        $westernWidths = StandardFontWesternMetrics::WIDTHS[$baseFont] ?? [];

        foreach (self::characters($text) as $character) {
            $width += $widths[$character]
                ?? $westernWidths[$character]
                ?? self::FALLBACK_GLYPH_WIDTH;
        }

        return ($width / 1000) * $size;
    }

    /**
     * @return list<string>
     */
    public static function glyphNames(string $baseFont): array
    {
        if ($baseFont === StandardFont::SYMBOL->value) {
            return StandardFontGlyphMap::glyphNames($baseFont);
        }

        if ($baseFont === StandardFont::ZAPF_DINGBATS->value) {
            return StandardFontGlyphMap::glyphNames($baseFont);
        }

        return StandardFontCoreGlyphMetrics::glyphNames($baseFont);
    }

    public static function glyphWidth(string $baseFont, string $glyphName): ?int
    {
        if ($baseFont === StandardFont::SYMBOL->value) {
            $code = StandardFontGlyphMap::glyphCodeForName($baseFont, $glyphName);

            return $code === null
                ? null
                : (self::SYMBOL_WIDTHS[$code] ?? null);
        }

        if ($baseFont === StandardFont::ZAPF_DINGBATS->value) {
            $code = StandardFontGlyphMap::glyphCodeForName($baseFont, $glyphName);

            return $code === null
                ? null
                : (self::ZAPF_DINGBATS_WIDTHS[$code] ?? null);
        }

        return StandardFontCoreGlyphMetrics::widthForGlyph($baseFont, $glyphName);
    }

    /**
     * @param list<string> $glyphNames
     */
    public static function measureGlyphNamesWidth(string $baseFont, array $glyphNames, float $size): ?float
    {
        $width = 0;

        foreach ($glyphNames as $glyphName) {
            $glyphWidth = self::glyphWidth($baseFont, $glyphName);

            if ($glyphWidth === null) {
                return null;
            }

            $width += $glyphWidth;
        }

        return ($width / 1000) * $size;
    }

    /**
     * @return array<string, int>
     */
    private static function buildCourierWidths(): array
    {
        $widths = [];

        for ($code = 32; $code <= 126; $code++) {
            $widths[chr($code)] = 600;
        }

        return $widths;
    }

    /**
     * @param array<string, string> $byteMap
     * @param array<int, int> $widths
     */
    private static function measureMappedTextWidth(string $text, float $size, array $byteMap, array $widths): float
    {
        $width = 0;

        foreach (self::characters($text) as $character) {
            if (preg_match('/^[\x09\x0A\x0D\x20]$/', $character) === 1) {
                $code = ord($character);
                $width += $widths[$code] ?? self::FALLBACK_GLYPH_WIDTH;

                continue;
            }

            $byte = $byteMap[$character] ?? null;

            if ($byte === null) {
                $width += self::FALLBACK_GLYPH_WIDTH;

                continue;
            }

            $width += $widths[ord($byte)] ?? self::FALLBACK_GLYPH_WIDTH;
        }

        return ($width / 1000) * $size;
    }

    /**
     * @return list<string>
     */
    private static function characters(string $text): array
    {
        return preg_split('//u', $text, -1, PREG_SPLIT_NO_EMPTY) ?: str_split($text);
    }
